using System;
using System.Collections;
using System.Collections.Generic;

/// <summary>
/// Details of a user.
/// </summary>
public class UserDetails
{
    private string userName;
    public string UserName
    {
        get
        {
            return userName;
        }
        set
        {
            userName = value;
        }
    }

    private Guid userId;
    public Guid UserId
    {
        get
        {
            return userId;
        }
        set
        {
            userId = value;
        }
    }
    
    public UserDetails()
    {
    }

    public override string ToString()
    {
        return this.userName + ":" + this.userId.ToString();
    }
}
