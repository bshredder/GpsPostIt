using System;
using System.Collections;
using System.Collections.Generic;

/// <summary>
/// Details of a client who submits a pin.
/// </summary>
public class ClientInfo
{
    private string hostAddress;
    public string HostAddress
    {
        get { return hostAddress; }
        set { hostAddress = value; }
    }

    private string hostName;
    public string HostName
    {
        get { return hostName; }
        set { hostName = value; }
    }

    private string rawUrl;
    public string RawUrl
    {
        get { return rawUrl; }
        set { rawUrl = value; }
    }

    private string httpMethod;
    public string HttpMethod
    {
        get { return httpMethod; }
        set { httpMethod = value; }
    }

    private bool isSecureConnection;
    public bool IsSecureConnection
    {
        get { return isSecureConnection; }
        set { isSecureConnection = value; }
    }

    private int totalBytes;
    public int TotalBytes
    {
        get { return totalBytes; }
        set { totalBytes = value; }
    }

    private string rawUserAgentStr;
    public string RawUserAgentStr
    {
        get { return rawUserAgentStr; }
        set { rawUserAgentStr = value; }
    }

    private string userAgentStr;
    public string UserAgentStr
    {
        get { return userAgentStr; }
        set { userAgentStr = value; }
    }

    private string platform;
    public string Platform
    {
        get { return platform; }
        set { platform = value; }
    }

    private int browserMajorVersion;
    public int BrowserMajorVersion
    {
        get { return browserMajorVersion; }
        set { browserMajorVersion = value; }
    }

    private double browserMinorVersion;
    public double BrowserMinorVersion
    {
        get { return browserMinorVersion; }
        set { browserMinorVersion = value; }
    }

    private string mobileDeviceManufacturer;
    public string MobileDeviceManufacturer
    {
        get { return mobileDeviceManufacturer; }
        set { mobileDeviceManufacturer = value; }
    }

    private string mobileDeviceModel;
    public string MobileDeviceModel
    {
        get { return mobileDeviceModel; }
        set { mobileDeviceModel = value; }
    }

    public ClientInfo()
    {
    }
}
