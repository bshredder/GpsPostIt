using System;
using System.Data;
using System.Configuration;
using System.Web;
using System.Web.Security;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Web.UI.WebControls.WebParts;
using System.Web.UI.HtmlControls;

/// <summary>
/// Directs an attachment operation on a pin.
/// </summary>
public class AttachmentOperation
{
    private string attachmentId;
    public string AttachmentId
    {
        get
        {
            return attachmentId;
        }
        set
        {
            attachmentId = value;
        }
    }

    private string attachment;
    public string Attachment
    {
        get
        {
            return attachment;
        }
        set
        {
            attachment = value;
        }
    }

    private string attachmentName;
    public string AttachmentName
    {
        get
        {
            return attachmentName;
        }
        set
        {
            attachmentName = value;
        }
    }

    private string attachmentType;
    public string AttachmentType
    {
        get
        {
            return attachmentType;
        }
        set
        {
            attachmentType = value;
        }
    }

    private ServerConstants.AttachmentOperationType attachmentOperationType;
    public ServerConstants.AttachmentOperationType AttachmentOperationType
    {
        get
        {
            return attachmentOperationType;
        }
        set
        {
            attachmentOperationType = value;
        }
    }

    public AttachmentOperation()
    {
    }
}
