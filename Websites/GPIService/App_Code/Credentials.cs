using System;
using System.Data;
using System.Configuration;
using System.Web;
using System.Web.Security;
using System.Web.UI;
using System.Web.UI.WebControls;
using System.Web.UI.WebControls.WebParts;
using System.Web.UI.HtmlControls;

/// <summary>
/// Encapsulates credentials to be passed on to the GPI web service.
/// </summary>
public class Credentials
{
    private string userId;
    public string UserId
    {
        get
        {
            return userId;
        }
        set
        {
            userId = value;
        }
    }

    private string userName;
    public string UserName
    {
        get
        {
            return userName;
        }
        set
        {
            userName = value;
        }
    }

    private string password;
    public string Password
    {
        get
        {
            return password;
        }
        set
        {
            password = value;
        }
    }

    private string unsecuredServicesValidationToken;
    public string UnsecuredServicesValidationToken
    {
        get
        {
            return unsecuredServicesValidationToken;
        }
        set
        {
            unsecuredServicesValidationToken = value;
        }
    }

    private string securedServicesValidationToken;
    public string SecuredServicesValidationToken
    {
        get
        {
            return securedServicesValidationToken;
        }
        set
        {
            securedServicesValidationToken = value;
        }
    }

    public Credentials()
    {
    }
}
